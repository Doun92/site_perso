function hide_Show(liste_cacher_montrer) {
    if (liste_cacher_montrer.style.display === "none") {
        liste_cacher_montrer.style.display = "block";
    } else {
        liste_cacher_montrer.style.display = "none";
    }
};

function show_image(image) {
    if (image.style.display === "none") {
        image.style.display = "block";
    }
    else {
        image.style.display = "none";
    }
};